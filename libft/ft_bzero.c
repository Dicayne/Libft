#include "ft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t i;

	i = 0;
	if (n > 0)
	{
		while (i <= n - 1)
		{
			((unsigned char*)s)[i] = '\0';
			i++;
		}
	}
}
